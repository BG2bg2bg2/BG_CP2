#How do you create a new Git repository? You can create a Git repository by running `git init` inside a project folder. This initializes Git and creates a hidden .git folder. You can also create a repository on GitHub and clone it using `git clone <url>`.


# What are branches used for in a Git repository? Branches are used to work on new features or bug fixes without affecting the main code. They allow multiple people to work on different tasks at the same time. The main branch usually contains stable, production-ready code.


# How do you create a branch in a Git repository? You can create a branch using `git branch branch-name`. To create and switch to the branch at the same time, use `git checkout -b branch-name`. You can also create branches directly on GitHub.


# Who should review changes on GitHub before merging the content to the main branch? Changes should be reviewed by a team member, peer developer, or project maintainer. Reviews help ensure code quality, reduce bugs, and maintain consistent standards.


# How do you change what branch you are on in VS Code? You can click the branch name in the bottom-left corner of VS Code and select a new branch. You can also use the Command Palette and choose "Git: Checkout to..." Or use the terminal with `git checkout branch-name`.
